export default function genColours(n) {
  //..........................................................
  // Generate master random colour and convert to HSL
  let hsl = rgbToHsl(Math.floor((Math.random() * 32) + 32),
                     Math.floor((Math.random() * 32) + 32),
                     Math.floor((Math.random() * 32) + 32));
  //..........................................................
  // Now generate N more with a relationship to the first
  let colours = [];
  for (let i = 0; i < n; i++) {
    let offset = 1/n;
    hsl[0] = (hsl[0] + offset) % 1;
    // convert to rgb
    let rgb = hslToRgb(hsl[0], hsl[1], hsl[2]);
    // convert to hex
    let hex = rgbToHex(rgb[0], rgb[1], rgb[2]);
    // add to array of colours
    colours.push(hex);
  }
  return colours;
}

export function genRandomColour() {
  return rgbToHex(Math.floor(Math.random() * 255),
                  Math.floor(Math.random() * 255),
                  Math.floor(Math.random() * 255));
}

